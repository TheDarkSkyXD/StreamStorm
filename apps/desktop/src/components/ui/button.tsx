import { Slot } from "@radix-ui/react-slot";
import { cva, type VariantProps } from "class-variance-authority";
import * as React from "react";

import { cn } from "@/lib/utils";

const buttonVariants = cva(
  "inline-flex items-center justify-center whitespace-nowrap rounded-md text-sm font-medium transition-colors focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-[var(--color-ring)] disabled:pointer-events-none disabled:opacity-50",
  {
    variants: {
      variant: {
        default:
          "bg-[var(--color-storm-primary)] text-black hover:bg-[var(--color-storm-primary)]/90",
        secondary:
          "bg-[var(--color-background-tertiary)] text-[var(--color-foreground)] hover:bg-[var(--color-background-elevated)]",
        outline:
          "border border-[var(--color-border)] bg-transparent hover:bg-[var(--color-background-secondary)]",
        ghost: "hover:bg-[var(--color-background-secondary)]",
        twitch: "bg-[var(--color-twitch)] text-white hover:bg-[var(--color-twitch-dark)]",
        kick: "bg-[var(--color-kick)] text-black hover:bg-[var(--color-kick-dark)]",
        destructive: "bg-red-500 text-white hover:bg-red-600",
        link: "text-[var(--color-storm-primary)] underline-offset-4 hover:underline",
      },
      size: {
        default: "h-10 px-4 py-2",
        sm: "h-9 rounded-md px-3",
        lg: "h-11 rounded-md px-8",
        icon: "h-10 w-10",
      },
    },
    defaultVariants: {
      variant: "default",
      size: "default",
    },
  }
);

export interface ButtonProps
  extends React.ButtonHTMLAttributes<HTMLButtonElement>,
    VariantProps<typeof buttonVariants> {
  asChild?: boolean;
}

const Button = React.forwardRef<HTMLButtonElement, ButtonProps>(
  ({ className, variant, size, asChild = false, ...props }, ref) => {
    const Comp = asChild ? Slot : "button";
    return (
      <Comp className={cn(buttonVariants({ variant, size, className }))} ref={ref} {...props} />
    );
  }
);
Button.displayName = "Button";

export { Button, buttonVariants };
